public class IntegerRange {


	// Fields
	private Integer start;
	private Integer end;

	// Constructors

	public IntegerRange() {

	}

	public IntegerRange(Integer start, Integer end)
	{
		this.start = start;
		this.end = end;
	}


	public Integer getStart() {
		return start;
	}

	public void setStart(Integer start) {
		this.start = start;
	}

	public Integer getEnd() {
		return end;
	}

	public void setEnd(Integer end) {
		this.end = end;
	}
}