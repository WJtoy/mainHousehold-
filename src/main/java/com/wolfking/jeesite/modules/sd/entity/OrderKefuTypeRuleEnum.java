package com.wolfking.jeesite.modules.sd.entity;

/**
 * @version 1.0.0
 * 工单客服类型与找客服规则
 */
public enum OrderKefuTypeRuleEnum {

    ORDER_COMMON(0,"大客服部"),
    ORDER_AUTO(1,"自动客服部"),
    ORDER_RUSH(2,"突击客服部");

    public Integer code; //
    public String msg;

    OrderKefuTypeRuleEnum(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

}
