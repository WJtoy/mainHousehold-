package com.wolfking.jeesite.modules.md.entity;

import com.wolfking.jeesite.modules.sys.entity.Dict;

import java.util.List;

/**
 * 自定義類
 * 
 * @author
 * @version 2014-09-24
 */

public class ProductPrices
{

	private Long id;
	private Product product;
	private List<ProductPrice> listProductPrice;
	private Dict priceType;

	public ProductPrices() {
	}

	public ProductPrices(Long id, Product product, List<ProductPrice> listProductPrice){
		this.id = id;
		this.product = product;
		this.listProductPrice = listProductPrice;
	}

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public Product getProduct()
	{
		return this.product;
	}

	public void setProduct(Product product)
	{
		this.product = product;
	}

	public List<ProductPrice> getListProductPrice()
	{
		return listProductPrice;
	}

	public void setListProductPrice(List<ProductPrice> listProductPrice)
	{
		this.listProductPrice = listProductPrice;
	}


	public Dict getPriceType() {
		return priceType;
	}

	public void setPriceType(Dict priceType) {
		this.priceType = priceType;
	}
}