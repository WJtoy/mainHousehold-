package com.wolfking.jeesite.modules.api.entity.sd.request;

//@JsonAdapter(RestOrderBaseRequestAdapter.class)
public class RestOrderBaseRequest {

    private String id;//id
    private String orderId;//数据库id
    private String quarter;//数据库分片
    private String remarks;
    private String orderNo;

    public RestOrderBaseRequest(){}

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getQuarter() {
        return quarter;
    }

    public void setQuarter(String quarter) {
        this.quarter = quarter;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }
}
